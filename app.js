button = document.querySelector('#next');
imageDisplay = document.querySelector('#imageDisplay');
profileDisplay = document.querySelector('#profileDisplay');

let cat = createImageVal();

function getNum() {
    return Math.floor((Math.random() * 199) + 1);
}


function* createImageVal() {
    let imageVal = getNum();

    while(true) {
        yield imageVal++
    }
}


button.onclick = () => {
    fetch('https://randomuser.me/api/')
        .then(result => result
        .json().then(data => nextCat(data.results[0].name, data.results[0].location)));
}

function nextCat(owner, location) {
    
    imageDisplay.innerHTML = `<img src="https://robohash.org/${cat.next().value}?set=set4" alt="cartoon cat">`
    
    profileDisplay.innerHTML = `
    <ul class="list-group">
        <li class="list-group-item"><strong>Cat Name:</strong> ${names[getNum()]}</li>
        <li class="list-group-item"><strong>Owner:</strong> ${owner.title} ${owner.first} ${owner.last}</li>
        <li class="list-group-item"><strong>Lives:</strong> ${location.city}, ${location.country}</li>
    </ul>
    `
}
